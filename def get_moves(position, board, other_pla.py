def get_moves(position, board, other_player_pos):
    # position: مكان اللاعب الحالي (صف، عمود)
    # board: لوحة اللعبة
    # other_player_pos: مكان اللاعب الآخر (الخصم)

    """ PLUS movement only """
    # تعليق بيشرح إن الحركة مسموح بها في اتجاهات + فقط (فوق، تحت، يمين، شمال)

    r, c = position
    # r = رقم الصف
    # c = رقم العمود

    moves = []
    # إنشاء قائمة فاضية علشان نحط فيها كل الحركات المسموح بها

    directions = [(-1,0), (1,0), (0,-1), (0,1)]
    # قائمة بالاتجاهات المسموح بها

    for dr, dc in directions:
        # حلقة بتمر على كل اتجاه
        # dr = delta row
        # dc = delta column

        nr, nc = r + dr, c + dc
        # حساب المكان الجديد لو اللاعب اتحرك في الاتجاه الحالي
        # nr = الصف الجديد
        # nc = العمود الجديد

        if 0 <= nr < SIZE and 0 <= nc < SIZE:
            # التأكد إن المكان الجديد جوا حدود اللوحة
            # لو خرج برا اللوحة → الحركة غير صالحة

            if board[nr][nc] == EMPTY and (nr, nc) != other_player_pos:
                # شرطين لازم يتحققوا:
                # 1. الخانة الجديدة فاضية (مش BLOCK)
                # 2. الخانة الجديدة مش مكان اللاعب الآخر

                moves.append((nr, nc))
                # لو الشرطين اتحققوا → نضيف الحركة للقائمة

    return moves
